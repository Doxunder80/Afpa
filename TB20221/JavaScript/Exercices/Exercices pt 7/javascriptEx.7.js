// Exercice 3 - Calculette //

var nombre1 = prompt("Entrez un premier nombre")
var nombre2 = prompt("Entrez un deuxième nombre")
var operateur = prompt("Entrez un opérateur: (+, -, * ou /)")

switch(operateur)
{
    case '+' :
        r1 =    Number(nombre1) + Number(nombre2)
        alert("Le résultat de l'opération est " + r1)
    break

    case '-' :
        r1 =    Number(nombre1) - Number(nombre2)
        alert("Le résultat de l'opération est " + r1)
    break

    case '*' :
        r1 =    Number(nombre1) * Number(nombre2)
        alert("Le résultat de l'opération est " + r1)
    break

    case '/' :
        nombre1 == 0 || nombre2 == 0
        alert("Erreur!")
    break

    case '/' :
        r1 =    Number(nombre1) / Number(nombre2)
        alert("Le résultat de l'opération est " + r1)
    break

    default :
        alert("Erreur!")
}

